          <h2 align="center">¿Qué es la cormobilidad?</h2>
        </div>
         <p class="intro">
           La forma más simplista de entender el término comorbilidad, seria referirla a la presentación en un mismo individuo de dos o más
           enfermedades o trastornos distintos.
           Cuando nos encontramos con un TDAH con comorbilidades se complica el diagnóstico, empeora la evolución y
           puede que exista una menor respuesta al tratamiento:
          </p>
           <img src="images/TDAHT/flecha.png" width="100px" height="100px" alt="flecha para indicar">
           <h3 class="fuente"> Se complica el diagnostico </h3>
        <p class="intro">Si se juntan varios trastornos, a la hora de realizar el diagnóstico es más difícil saber cuáles son los síntomas principales,
          cuales son causados por un trastorno o por otro, y cuál es el trastorno principal. Por eso es de vital importancia acudir a un especialista que pueda hacer las pruebas clínicas oportunas para hacer un diagnóstico certero. </p>
          <br>
          <img src="images/TDAHT/flecha.png" width="100px" height="102px" alt="flecha para indicar">
          <h3> Empeoro de evolución </h3>
          <p class="intro">Cuando se juntan varios trastornos, también su evolución se hace más difícil. En estos casos, no sólo nos
            enfrentamos a los síntomas propios de un trastorno, si no a un conjunto de síntomas añadidos que acentúan los problemas
            propios del TDAH y que, además, requieren tratamientos diferentes. El abordaje se complica y las mejoras son más difíciles
            de alcanzar.</p>
          <br>
          <img src="images/TDAHT/flecha.png" width="100px" height="102px" alt="flecha para indicar">
          <h3> Puede disminuir la respuesta al tratamiento</h3>
          <p class="intro"> Cuando se presenta un caso de TDAH con trastornos asociados, nos enfrentamos a diversos síntomas y patologías que requieren tratamientos específicos.
            Si no se tratan los otros trastornos asociados correctamente, puede verse disminuida la respuesta al tratamiento del TDAH.
            Se recomienda ir con un especialista para más información acerca de los tratamientos </p>

          <div class="page-header">
          <h2 align="center">Trastornos comórbidos asociados al TDAH según el DSM</h2>
        </div>
          <p class="intro">
          Estas son solo algunos de los trastornos más comunes entre los pacientes del TDAH.
        </p> <br>
          <img src="images/TDAHT/52.png" width="160px" heigth="150px" alt="52% de trastorno por uso de substancias">
          <h3>Trastorno por Uso de Substancias (TUS) </h3>
          <p class="intro">Tener TDAH aumenta el riesgo de desarrollar un trastorno por consumo de sustancias adictivas. Además el TDAH se ha asociado con un inicio más temprano del abuso de sustancias, con un mayor número de diagnósticos comórbidos de TUS y con mayores dificultades para mantener la abstinencia tras las desintoxicaciones.
            <br> <br>
          Resulta complicado identificar a los pacientes con abuso de sustancias que también padecen TDAH, por que a menudo se asocian a
           otros trastornos psiquiátricos que se solapan con muchos de los síntomas del TDAH. Se ha estimado que un 52% de pacientes
           diagnosticados con TDAH presentan un trastorno por uso de substancias </p>
           <br>
                  <img class="sin" src="images/TDAHT/40.png" width="160px" heigth="150px" alt="40% de trastorno Negativista desafiante">
                <h3>Trastorno Negativista Desafiante (TND) </h3>
                    <p class="intro"> Se caracteriza por un patrón recurrente e inapropiado, para el nivel de desarrollo y contexto socio-cultural, de conductas negativistas, desafiantes,
                    desobedientes y comportamiento hostil hacia las figuras de autoridad como sus padres y/o tutores.
                    <br>
                    <p>Entre la población con TDAH, la prevalencia de comorbilidad con TND está entre el <strong> 40% y 50% </strong></p>
                    <br>
                  <img src="images/TDAHT/40.png" width="160px" heigth="150px" alt="40% de trastorno Conducta Disocial">
                <h3>Trastorno de Conducta Disocial (TC) </h3>
                    <p class="intro"> se caracteriza por: agresión a personas o animales,estos pacientes provocan peleas, utilizan armas e intimidan a otras personas, destrucción de la propiedad,
                    engaños y robos graves, y violaciones serias de los derechos de otros y de las normas sociales. En la época adolescente pueden saltarse las clases, escaparse de casa y
                    dormir fuera de casa sin consentimiento.
                    <br><br>
                    La prevalencia de TC en pacientes con TDAH llega a ser hasta del <strong> 20-50% </strong> en niños y del 44-50% en adolescentes.
                    <br><br>
                    <img src="images/TDAHT/27.png" width="160px" heigth="150px" alt="27% trastorno depresivo">
                <h3>Trastorno Depresivo </h3>
                  <img class="centro" src="images/s2.jpg" width="700px" heigth="900px" alt="Niño">
                    <p class="intro">
                    Se caracteriza por un estado depresivo crónico, que se manifiesta la mayor parte del día la mayoría de los días.
                     En niños y adolescentes, pueden estar irritables y presentar mínimo dos los siguientes síntomas: pérdida o aumento del apetito, falta de energía,
                     baja autoestima, dificultades para concentrarse, insomnio o hipersomnia. Los síntomas de la depresión pueden fácilmente confundir al observador, puesto que la agitación se puede confundir con hiperactividad y el retraso psicomotor con la falta de atención.
                     <br><br> La prevalencia de este trastorno es de un 26,8%.
                     <br><br>
                     <img src="images/TDAHT/40.png" width="160px" heigth="150px" alt="27% trastorno depresivo">
                 <h3>Trastorno de Ansiedad </h3>
                    <p class="intro"> La mayoría de los niños con TDAH presenta algunos síntomas de ansiedad como miedo a dormir solos o a separarse de los
                        padres, preocupación excesiva por la salud y el bienestar de su familia, molestias físicas cuando van al colegio, etc., que
                        suelen estar relacionados con el fracaso escolar y las críticas recibidas de sus padres, amigos y profesores.
                        <br> <br> El trastorno de ansiedad tiene una prevalencia de 34% en niños con TDAH pasando a un 40% en la población adulta con TDAH</p>
                       <br><br>

                        <img src="images/TDAHT/33.png" width="160px" heigth="150px" alt="33% trastorno del espectro autista">
                    <h3>Trastorno del Espectro Autista (TEA)</h3>
                    <p class="intro"> Presentan problemas de interacción social, problemas de comunicación y un patrón repetitivo de conductas6. Según un estudio, el 33% de los niños con TEA presentan síntomas de TDAH de tipo inatento,
                      el 26% de tipo combinado (déficit de atención e hiperactividad/impulsividad), el otro 41% no presenta comorbilidad. </p
                      <br><br>
                      <img src="images/TDAHT/20.png" width="160px" heigth="150px" alt="20% Trastorno del Aprendizaje">
                      <h3>Trastorno del Aprendizaje</h3>
                      <p class="intro"> Interfiere significativamente en el rendimiento escolar o en las actividades de la vida cotidiana que exigen habilidades para la lectura, escritura, cálculo matemático y desarrollo del lenguaje narrativo. Afecta también a la productividad y fluidez en el desarrollo de tareas que requieran organización.
                        Presentan un vocabulario muy limitado, mala construcción de las frases y conjugan mal los tiempos verbales. Se puede decir que es de las más
                        comunes en los niños.
                        <br> <br>
                        Los niños con TDAH que presentan mayor nivel de déficit de atención tienen más problemas asociados a trastornos de aprendizaje, que los que son hiperactivos-impulsivos. Se estima que afecta al 20% de los niños con TDAH.
                      </p>
                      <br>
                      <img src="images/TDAHT/11.png" width="160px" heigth="150px" alt="11% Trastorno de Tics (Tourette)">
                      <h3> Trastorno de Tics (Tourette)</h3>
                      <p class="intro"> Un tic motor es un movimiento rápido, involuntario, reiterado y arrítmico que afecta a un músculo o a un grupo
                        de músculos. El estrés aumenta su frecuencia de reproducción y desaparecen durante el sueño. Este trastorno crea un gran malestar y deterioro de las relaciones sociales.
                        <br>
                        Un 11% de los niños con TDAH padece este tipo de trastorno asociado. </p>
                        <br>
                        <img src="images/TDAHT/10.png" width="160px" heigth="150px" alt="10% Trastorno Bipolar">
                        <h3> Trastorno Bipolar</h3>
                        <p class="intro"> El niño o adolescente con Trastorno Bipolar, se caracteriza por haber padecido más de dos episodios en los que su estado de ánimo y nivel de actividad parecen alterados. Cambios de humor extremos,
                          aumento del nivel de energía, pensamiento acelerado, disminución del sueño, síntomas psicóticos como alucinaciones o delirios.
                          <br>
                          La relación entre TDAH y enfermedad bipolar es compleja y controvertida. Esto se debe en parte a que algunos síntomas son comunes a ambas patologías tales como:
                          hiperactividad, impulsividad, inatención, distraibilidad, irritabilidad, problemas de sueño y capacidad de juicio disminuida.
                           La prevalencia de este trastorno asociado al TDAH es del 10% siendo más frecuente en los niños. </p>
