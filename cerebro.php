            <h3 align="center"> Qué sucede en el cerebro de los niños con TDAH </h3>
          <div class=" animatedParent">
            <p class="intro animated fadeInUpShort">
              Como ya se menciono previamente el trastorno por déficit de atención e hiperactividad (TDAH) se caracteriza por dificultades
              para mantener la atención, hiperactividad o exceso de movimiento, e impulsividad o dificultad en el control de los impulsos.
              <br>
              El TDAH fecta a aproximadamente 1 de cada 20 niños, puede tener una gran impacto en la vida cotidiana. Las imágenes cerebrales
              han mostrado que los síntomas de TDAH están asociados con una serie de anomalías en el desarrollo y funcionamiento de algunas partes del cerebro.
              <br>
              <img class="k" src="images/cerebro/8.png">
            </p>
            <p class="intro animated fadeInUpShort">
              Sin embargo, es importante aclarar que el cerebro de un niño con TDAH no está dañado ni es defectuoso. Se trata de un cerebro que funciona con sus
              propias reglas, es decir, a pesar de que estos niños tiene problemas de aprendizaje, la gran mayoría tienen un coeficiente intelectual significativamente
              superior a la media. Pero utilizan esta capacidad intelectual de una forma diferente a como la utiliza un niño sin este tipo de trastorno.
              <br>
              Las investigaciones más recientes indican que el TDAH se asocia con una disminución global del volumen cerebral. Existe una reducción del volumen del lóbulo frontal,
              de los ganglios basales y del cerebelo, áreas críticas en la función ejecutiva, en la atención y en la capacidad de ejercer la inhibición.
              <br>
              En este vídeo se explica de una forma sencilla qué es lo que sucede en el cerebro de un niño que sufre trastorno por déficit de atención e hiperactividad.
